package ro.vladbutnaru.financeadvisor.entities;

public class Spending {

	private double amount;
	
	private int dayOfSpending;
	
	public double getAmount() {
		return amount;
	}
	public void setAmount(double amount) {
		this.amount = amount;
	}
	public int getDayOfSpending() {
		return dayOfSpending;
	}
	public void setDayOfSpending(int dayOfSpending) {
		this.dayOfSpending = dayOfSpending;
	}
	
	
}
